/** 
 * @file scancodes.h
 * @brief Defines several useful scancode macros
 * 
 * This file defines useful macros for scancodes used in input event handling
 */


#ifndef _SCANCODES_PROJ_H_
#define _SCANCODES_PROJ_H_

// WASD
#define KEY_MK_W 0x11 ///< W key makecode
#define KEY_BK_W 0x91 ///< W key breakcode
#define KEY_MK_A 0x1E ///< A key makecode
#define KEY_BK_A 0x9E ///< A key breakcode
#define KEY_MK_S 0x1F ///< S key makecode
#define KEY_BK_S 0x9F ///< S key breakcode
#define KEY_MK_D 0x20 ///< D key makecode
#define KEY_BK_D 0xA0 ///< D key breakcode

// Arrow keys


// Enter key (main Enter)
#define KEY_MK_ENTER  0x1C ///< Enter key makecode
#define KEY_BK_ENTER  0x9C ///< Enter key breakcode

#endif /*_SCANCODES_PROJ_H_ */
